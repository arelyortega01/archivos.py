# Datos ya asignados
numero = 10
texto = "Arely"
decimal = 3.14159

# Uso de bin(): convierte un número a binario
binario = bin(numero)

# Uso de len(): cuenta la cantidad de caracteres de un texto
longitud = len(texto)

# Uso de round(): redondea un número decimal
redondeado = round(decimal, 2)

# Mostrar resultados
print("Número original:", numero)
print("Número en binario:", binario)

print("Texto:", texto)
print("Cantidad de letras:", longitud)

print("Número decimal:", decimal)
print("Número redondeado:", redondeado)